                 Las tapitas se convierten en fondos para ayudar a la Fundación Garrahan
                  </p>        
  <p>
  Red del Hogar propone q sus asociados participar de esta acción para recuperar miles de sonrisas.
Bajo el lema de “Doná tapitas, Destapá sonrisas” se decidió  participar junto al Club de Leones en el programa de Reciclado para ayudar al Hospital de Pediatría Garrahan. 
El objetivo es  trabajar en conjunto motivando en la gente la promoción de la salud, la solidaridad y el cuidado del medio ambiente. <br><br>

Las tapitas son fundamentales porque se convierten en fondos para ayudar a la Fundación y a los niños internados.
Nuestros comercios se han transformado en bocas de recolección para recepcionar las tapitas.
Gracias al apoyo otorgado por empresas de primer nivel como Panasonic, Visuar (Samsung) y Frimeral (Gafa) hemos logrado entregar afiches gratuitos a nuestros asociados para difundir la acción en los comercios.
Actualmente hemos logrado  entregar a la fundación 500kg de plástico.
De esta forma colaboramos con los niños del Hospital Garrahan y evitamos provocar un impacto negativo en el medio ambiente </p>
<p>
*Aclaración: La recolección de tapitas no implica ningún tipo de donación de dinero a la Fundación Garrahan por parte de ninguna de las empresas mencionadas.
Más información sobre la Fundación: www.vaporlospibes.com.ar


